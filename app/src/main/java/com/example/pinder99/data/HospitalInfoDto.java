package com.example.pinder99.data;

public class HospitalInfoDto {
    private String 치료소명;
    private String 지정일;
    private String 주소;
    private String 구분;

    public String get치료소명() { return 치료소명; }
    public void set치료소명(String 치료소명) { this.치료소명 = 치료소명; }

    public String get지정일() { return 지정일; }
    public void set지정일(String 지정일) { this.지정일 = 지정일; }

    public String get주소() { return 주소; }
    public void set주소(String 주소) { this.주소 = 주소; }

    public String get구분() { return 구분; }
    public void set구분(String 구분) { this.구분 = 구분; }
}
